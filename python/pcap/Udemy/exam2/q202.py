# Which statement is true about the Python standard input, output and error streams ?


# The three predefined streams are :

# - the standard input (stdin) - usually associated with the keyboard - for example : the input() function reads data from stdin

# - the standard output (stdout) - associated with the screen - for example : the print() function writes data to the stdout stream.

# - the standard error output (stderr) - associated with the screen

# These three streams are pre-opened by default - there is no need to call open() to open them. There is also no need to close them.

# These streams can be used explicitly with the help of the sys module.